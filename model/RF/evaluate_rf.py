#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""
Random Forest Evaluation with Fixed Best Parameters
=====================================================

This script evaluates Random Forest models using pre-determined optimal parameters
across multiple scaffold splits. It collects statistics across different random seeds
to provide robust performance estimates.

Features:
- Load best parameters from JSON files (generated by bayes_rf.py)
- Multiple scaffold splits with different seeds
- Accept only splits within tolerance of target train/test ratio
- Support for regression and classification tasks
- Support for multi-task datasets

Usage:
    # Regression task
    python evaluate_rf.py --dataset esol --descriptor esol_nsc500_sl20.pkl --params params_measured_log_sol.json

    # Classification task
    python evaluate_rf.py --dataset bace --descriptor bace_nsc500_sl20.pkl --params params_Class.json

    # Multi-task with auto params detection
    python evaluate_rf.py --dataset tox21 --descriptor tox21_nsc500_sl20.pkl --auto-params
"""

import os
import sys
import json
import math
import pickle
import random
import argparse
from pathlib import Path

import numpy as np
import pandas as pd

from rdkit import Chem
from rdkit.Chem.Scaffolds import MurckoScaffold

from sklearn.ensemble import RandomForestClassifier, RandomForestRegressor
from sklearn.metrics import (
    roc_auc_score, average_precision_score, mean_squared_error,
    mean_absolute_error, r2_score
)


# =============================================================================
# Scaffold Splitting Functions
# =============================================================================

def get_ring_count(smiles: str, max_rings: int = 5) -> int:
    """Get ring count for a SMILES string."""
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return 0
    n_rings = mol.GetRingInfo().NumRings()
    return min(n_rings, max_rings)


def get_scaffold(smiles: str) -> str:
    """Get Murcko scaffold for a SMILES string."""
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return None
    return MurckoScaffold.MurckoScaffoldSmiles(mol=mol)


def scaffold_split(df_smiles: pd.DataFrame, frac_train: float, frac_test: float, 
                   seed: int, max_rings: int = 5):
    """
    Perform scaffold-based train/test split.
    
    Parameters
    ----------
    df_smiles : pd.DataFrame
        DataFrame with 'smiles' column
    frac_train : float
        Fraction of data for training
    frac_test : float
        Fraction of data for testing
    seed : int
        Random seed
    max_rings : int
        Maximum ring count to consider
        
    Returns
    -------
    train_idx, test_idx : np.ndarray
        Indices for train and test sets
    """
    rng = random.Random(seed)
    ring_groups = {}
    scaffold_groups = {}

    for i, smi in enumerate(df_smiles["smiles"].values):
        mol = Chem.MolFromSmiles(smi)
        if mol is None:
            continue
        n_rings = get_ring_count(smi, max_rings)
        scaffold = get_scaffold(smi)
        if scaffold is None:
            continue
        ring_groups.setdefault(n_rings, []).append(i)
        scaffold_groups.setdefault(n_rings, {}).setdefault(scaffold, []).append(i)

    print("Ring count distribution:")
    for n_rings, idxs in sorted(ring_groups.items()):
        label = f">={max_rings} rings" if n_rings == max_rings else f"{n_rings} rings"
        print(f"  {label}: {len(idxs)} molecules")

    train_idx, test_idx = [], []
    random_split_layers = {}

    for n_rings, scf_dict in reversed(list(scaffold_groups.items())):
        scf_list = list(scf_dict.keys())

        if len(scf_list) <= 3:
            print(f"Layer {n_rings}: {len(scf_list)} scaffolds too few, using random split.")
            idxs = ring_groups[n_rings]
            random_split_layers[n_rings] = idxs
            rng.shuffle(idxs)
            n_total = len(idxs)
            n_test = round(frac_test * n_total)
            train_idx.extend(idxs[n_test:])
            test_idx.extend(idxs[:n_test])
            continue

        rng.shuffle(scf_list)
        n_total = len(scf_list)
        n_test = round(frac_test * n_total)
        test_scf = scf_list[:n_test]
        train_scf = scf_list[n_test:]

        train_mols, test_mols = [], []
        for s in train_scf:
            train_mols.extend(scf_dict[s])
        for s in test_scf:
            test_mols.extend(scf_dict[s])

        print(f"Layer {n_rings}: {len(train_scf)} scaffolds ({len(train_mols)} mols) train, "
              f"{len(test_scf)} ({len(test_mols)}) test")

        train_idx.extend(train_mols)
        test_idx.extend(test_mols)

    n_train, n_test = len(train_idx), len(test_idx)
    n_total = n_train + n_test
    print(f"\nTotal molecules: {n_total}")
    print(f"Train: {n_train} ({n_train/n_total:.2%})")
    print(f"Test : {n_test} ({n_test/n_total:.2%})")

    # Adjust ratios if deviation > 2%
    target_frac = np.array([frac_train, frac_test])
    current_counts = np.array([len(train_idx), len(test_idx)])
    current_frac = current_counts / current_counts.sum()

    deviation = target_frac - current_frac
    if np.any(np.abs(deviation) > 0.02):
        print("\nAdjusting using random-split layers to fix ratio...")
        all_random_idxs = []
        for idxs in random_split_layers.values():
            all_random_idxs.extend(idxs)
        rng.shuffle(all_random_idxs)

        total = len(train_idx) + len(test_idx)
        target_counts = (target_frac * total).astype(int)

        while len(train_idx) > target_counts[0] and all_random_idxs:
            i = all_random_idxs.pop()
            if i in train_idx:
                train_idx.remove(i)
                test_idx.append(i)

        while len(train_idx) < target_counts[0] and all_random_idxs:
            i = all_random_idxs.pop()
            if i not in train_idx and i not in test_idx:
                train_idx.append(i)

        print(f"After adjustment: Train {len(train_idx)/total:.2%}, Test {len(test_idx)/total:.2%}")

    return np.array(train_idx), np.array(test_idx)


# =============================================================================
# Task Detection
# =============================================================================

def detect_task_type(labels: np.ndarray) -> str:
    """Auto-detect if task is classification or regression."""
    unique_vals = np.unique(labels[~np.isnan(labels)])
    
    if set(unique_vals).issubset({0.0, 1.0, 0, 1}):
        return "classification"
    
    if len(unique_vals) < 10:
        return "classification"
    
    return "regression"


# =============================================================================
# Parameter Loading
# =============================================================================

def load_params(params_file: Path, task_name: str = None):
    """
    Load best parameters from JSON file.
    
    Parameters
    ----------
    params_file : Path
        Path to JSON file with parameters
    task_name : str, optional
        Task name for multi-task datasets
        
    Returns
    -------
    dict
        Parameter dictionary
    """
    if not params_file.exists():
        raise FileNotFoundError(f"Parameters file not found: {params_file}")
    
    with open(params_file, "r", encoding="utf-8") as f:
        params = json.load(f)
    
    return params


def find_params_file(output_dir: Path, task_name: str):
    """Find parameters file for a task."""
    # Try different naming conventions
    candidates = [
        output_dir / f"params_{task_name}.json",
        output_dir / f"params_{task_name.lower()}.json",
        output_dir / f"{task_name}_params.json",
    ]
    
    for candidate in candidates:
        if candidate.exists():
            return candidate
    
    return None


# =============================================================================
# Evaluation Functions
# =============================================================================

def evaluate_classification_single(model, X_test, y_test):
    """Evaluate classification model."""
    pos_idx = int(np.where(model.classes_ == 1)[0][0])
    y_prob = model.predict_proba(X_test)[:, pos_idx]
    auc = roc_auc_score(y_test, y_prob)
    ap = average_precision_score(y_test, y_prob)
    return {"roc_auc": auc, "pr_auc": ap}


def evaluate_regression_single(model, X_test, y_test):
    """Evaluate regression model."""
    y_pred = model.predict(X_test)
    rmse = math.sqrt(mean_squared_error(y_test, y_pred))
    mae = mean_absolute_error(y_test, y_pred)
    r2 = r2_score(y_test, y_pred)
    return {"rmse": rmse, "mae": mae, "r2": r2}


